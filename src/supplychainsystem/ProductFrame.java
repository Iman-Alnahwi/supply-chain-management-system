    private void loadCategories() {
        cmbCategory.removeAllItems();
        for (String c : Product.ALLOWED_CATEGORIES) {
            cmbCategory.addItem(c);
        }
    }

    
    private void loadSuppliers() {
        cmbSuppliers.removeAllItems();
        supplierList = supplierDB.getAllSuppliers();
        for (Supplier s : supplierList) {
            cmbSuppliers.addItem(s.getName());
        }
    }

    
    private void loadProducts() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"ID", "Name", "Category", "Price", "Stock", "Supplier"}, 0);
        ArrayList<Product> list = db.getAllProducts();
        for (Product p : list) {
            model.addRow(new Object[]{
                p.getProductId(), p.getName(), p.getCategory(),
                p.getUnitPrice(), p.getStockQuantity(), p.getSupplierName()
            });
        }
        tblProducts.setModel(model);
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = txtName.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a product name.");
            return;
        }

        
        double price;
        try {
            price = Double.parseDouble(txtPrice.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price must be a number (example: 19.99).",
                "Invalid Price", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (price < 0) {
            JOptionPane.showMessageDialog(this, "Price cannot be negative.");
            return;
        }

       
        int stock;
        try {
            stock = Integer.parseInt(txtStock.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stock must be a whole number (example: 50).",
                "Invalid Stock", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (stock < 0) {
            JOptionPane.showMessageDialog(this, "Stock cannot be negative.");
            return;
        }

       
        int supIndex = cmbSuppliers.getSelectedIndex();
        if (supIndex == -1) {
            JOptionPane.showMessageDialog(this, "Please add a supplier first.");
            return;
        }
        int supplierId = supplierList.get(supIndex).getId();
        String category = cmbCategory.getSelectedItem().toString();

        Product p = new Product(0, name, category, price, stock, supplierId);
        if (db.addProduct(p)) {
            JOptionPane.showMessageDialog(this, "Product added successfully.");
            loadProducts();
            txtName.setText("");
            txtPrice.setText("");
            txtStock.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add product.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

