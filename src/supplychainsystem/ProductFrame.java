    private void tblProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductsMouseClicked
        int row = tblProducts.getSelectedRow();
        if (row == -1) return;   // nothing selected
        txtName.setText(tblProducts.getValueAt(row, 1).toString());
        cmbCategory.setSelectedItem(tblProducts.getValueAt(row, 2).toString());
        txtPrice.setText(tblProducts.getValueAt(row, 3).toString());
        txtStock.setText(tblProducts.getValueAt(row, 4).toString());
        if (tblProducts.getValueAt(row, 5) != null) {
            cmbSuppliers.setSelectedItem(tblProducts.getValueAt(row, 5).toString());
        }

        // If the selected product is low on stock (5 or less), warn the user
        int stock = Integer.parseInt(tblProducts.getValueAt(row, 4).toString());
        if (stock <= 5) {
            String productName = tblProducts.getValueAt(row, 1).toString();
            JOptionPane.showMessageDialog(this,
                "Low stock warning\n\n"
                + "Product : " + productName + "\n"
                + "Only " + stock + " unit(s) left in stock.\n"
                + "Please order more from the supplier.",
                "Low Stock", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tblProductsMouseClicked

