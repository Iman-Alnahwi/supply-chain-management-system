    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a product from the table first.");
            return;
        }

        int    id   = (int) tblProducts.getValueAt(row, 0);
        String name = tblProducts.getValueAt(row, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete product: " + name + "?",
            "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        // Save a copy first so the Restore button can bring it back
        String category     = tblProducts.getValueAt(row, 2).toString();
        double price        = Double.parseDouble(tblProducts.getValueAt(row, 3).toString());
        int    stock        = Integer.parseInt(tblProducts.getValueAt(row, 4).toString());
        String supplierName = (tblProducts.getValueAt(row, 5) == null)
                            ? "" : tblProducts.getValueAt(row, 5).toString();
        int supplierId = 0;
        for (Supplier s : supplierList) {
            if (s.getName().equals(supplierName)) { supplierId = s.getId(); break; }
        }
        Product backup = new Product(id, name, category, price, stock, supplierId);

        if (db.deleteProduct(id)) {
            deletedProducts.add(backup);   // remember it for Restore
            DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
            model.removeRow(row);          // remove the row from the table
            JOptionPane.showMessageDialog(this,
                "Product deleted. Press Restore to bring it back.");
            txtName.setText("");
            txtPrice.setText("");
            txtStock.setText("");
        } else {
            JOptionPane.showMessageDialog(this,
                "Cannot delete this product.\n"
                + "It is used in one or more orders, so it cannot be removed.",
                "Delete Blocked", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnRestoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestoreActionPerformed
        if (deletedProducts.isEmpty()) {
            JOptionPane.showMessageDialog(this, "There is nothing to restore.");
            return;
        }
        // Bring back the last deleted product
        Product last = deletedProducts.get(deletedProducts.size() - 1);
        if (db.addProduct(last)) {
            deletedProducts.remove(deletedProducts.size() - 1);
            JOptionPane.showMessageDialog(this, "Restored: " + last.getName());
            loadProducts();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to restore product.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRestoreActionPerformed

